const reverseLookup = new Map();
Object.entries(abugida).forEach(([root, forms]) => {
    forms.forEach((char, index) => reverseLookup.set(char, { root, index }));
});
const vowelMap = { a: 3, e: 5, i: 2, o: 6, u: 1 };
function getBaseTransliteration(text) {
    let result = "";
    let lastRoot = null;
    for (let i = 0; i < text.length; i++) {
        const char = text[i].toLowerCase();
        const next = text[i + 1]?.toLowerCase();
        if (char in chars) {
            const roots = chars[char];
            let root = roots[0];
            if (next && /^[1-9]$/.test(next)) {
                root = roots[Math.min(Number(next) - 1, roots.length - 1)];
                i++;
            }
            result += abugida[root][0];
            lastRoot = root;
        }
        else if (char === 'i' && next === 'e' && lastRoot) {
            result = result.slice(0, -1) + abugida[lastRoot][4];
            i++;
        }
        else if (char in vowelMap && lastRoot) {
            result = result.slice(0, -1) + abugida[lastRoot][vowelMap[char]];
        }
        else {
            const existing = reverseLookup.get(text[i]);
            result += text[i];
            lastRoot = existing ? existing.root : null;
        }
    }
    return result;
}
